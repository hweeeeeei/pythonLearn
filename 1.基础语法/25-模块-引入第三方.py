# 第三方库都会在Python官方的pypi.python.org网站注册，要安装一个第三方库，
# 必须先知道该库的名称，可以在官网或者pypi上搜索，
# 比如Pillow的名称叫Pillow，因此，安装Pillow的命令就是：

# pip install Pillow


# 安装常用模块
# 在使用Python时，我们经常需要用到很多第三方库，例如，上面提到的Pillow，以及MySQL驱动程序，
# Web框架Flask，科学计算Numpy等。用pip一个一个安装费时费力，还需要考虑兼容性。
# 我们推荐直接使用Anaconda，这是一个基于Python的数据处理和科学计算平台，
# 它已经内置了许多非常有用的第三方库，我们装上Anaconda，就相当于把数十个第三方模块自动安装好了，非常简单易用。

# https://www.anaconda.com/
